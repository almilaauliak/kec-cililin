    <h3 class="mt-4">Asal-usul nama Cililin</h3>
    <p> merupakan nama desa atau kecamatan yang ada di Kabupaten  Bandung Barat (KBB). Namun, dibalik kata ‘Cililin’ sendiri ternyata tersimpan sejarah yang cukup panjang dan menarik untuk digali.</p>
    <p> Asal-usul nama Cililin Bandung ada dua versi. Versi pertama menurut Amar Sudarman, peneliti sejarah Cililin, mengatakan nama Cililin berasal dari bahasa Belanda, “Uit Tuin Lijn Weg” atau “Elina” yang berarti pembuatan jalan.</p>
    <p> Awalnya pada tahun 1840, Wedana Rongga Raksanegara I mendapat perintah dari Regent Residen Priangan untuk membuat jalan perkebunan kopi dari Cililin ke Gununghalu. Raksanegara I kemudian memerintahkan rakyat bergotong royong membuka jalan dari Kampung Kaca-kaca, Loji, dan Tangsi Gununghalu.</p>
    <p> Surat perintah pembuatan jalan ini diterbitkan dengan bahasa Belanda “uit tuin lijn weg”. Akhirnya dalam waktu 10 tahun jalan itu rampung dan Wedana Rongga Raksanegara I mendapat penghargaan dari Residen Priangan. “Uit tuin lijn weg itu bahasa Belanda yang artinya jalan lurus. Tapi pelafalan orang Sunda jadi keliru sehingga disebut Cililin.”</p>
    <p>Versi kedua nama Cililin Bandung menurut T. Bachtiar, penulis buku Toponimi: Susur Galur Nama Tempat di Jawa Barat. Beliau meyakini istilah Cililin berasal dari nama tumbuhan bernama Hoe Lilin (calamus melanoloma) atau Ki Lilin (Podocarpus amara BL/Sundacarpus amarus). Kedua pohon itu banyak tumbuh di wilayah yang sekarang disebut Cililin, sehingga masyarakat sekitar menyebut daerah itu Hoe Lilin atau Ki Lilin.</p>
    <p>Hoe Lilin atau calamus melanoloma merupakan tanaman hutan jenis rotan yang biasa tumbuh di daerah tropis. Sedangkan Ki Lilin atau Podocarpus amara BL/Sundacarpus amarus adalah pohon pohon besar yang selalu hijau sepanjang tahun dengan tinggi 10 hingga 60 meter dengan diameter batang 12 hingga 140 cm.</p>
